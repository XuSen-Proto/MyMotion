function R = quat2R(h,nh2)
h11 = h(1)*h(1)/nh2; h12 = 2*h(1)*h(2)/nh2; h13 = 2*h(1)*h(3)/nh2; h14 = 2*h(1)*h(4)/nh2;
h22 = h(2)*h(2)/nh2; h23 = 2*h(2)*h(3)/nh2; h24 = 2*h(2)*h(4)/nh2;
h33 = h(3)*h(3)/nh2; h34 = 2*h(3)*h(4)/nh2; h44 = h(4)*h(4)/nh2;
R = [h11+h22-h33-h44 h23-h14 h24+h13;...
    h23+h14 h11+h33-h22-h44 h34-h12;...
    h24-h13 h12+h34 h11+h44-h22-h33];
end